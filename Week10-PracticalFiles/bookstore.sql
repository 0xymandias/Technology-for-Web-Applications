

CREATE TABLE AUTHOR (
authorID CHAR(7) NOT NULL PRIMARY KEY,
firstname CHAR(15),
lastname CHAR(15),
birthdate DATE,
phone CHAR(10),
email CHAR(20),
address CHAR(36),
country CHAR(16));

CREATE TABLE PUBLISHER (
publishID CHAR(7) NOT NULL PRIMARY KEY,
name CHAR(15),
postaddress CHAR(35),
phone CHAR(10),
email CHAR(20),
region CHAR(16));

CREATE TABLE BOOK (
bookID CHAR(6) NOT NULL PRIMARY KEY,
booktitle CHAR(30),
ISBN CHAR(30),
price DECIMAL(8,2),
genre CHAR(15),
edition INT(2),
lang CHAR(15),
quantity INT(4),
publishID CHAR(7),
foreign key (publishID) references publisher(publishID)
);

CREATE TABLE WRITES (
bookID CHAR(6) NOT NULL,
authorID CHAR(7) NOT NULL,
constraint writepk primary key (bookID,authorID),
foreign key (bookID) references BOOK(bookID),
foreign key (authorID) references AUTHOR(authorID)
);


